const myFolders = document.getElementById("myFolders");
const myPics = document.getElementById("myPics");
if (myFolders && myPics) {
  Folders.forEach(f => {
    const fi = document.createElement("div");
    fi.info = f;
    fi.className = "folderitem";
    fi.textContent = `${f.title} (${f.pics.length})`;
    fi.onclick = function(e) {
      myPics.innerHTML = "";
      const name = this.info.name;
      const pics = this.info.pics;
      pics.forEach(filename => {
        const img = document.createElement("img");
        img.className = "pic";
        img.src = `Images%2F${name}%2F${filename}`;
        img.title = filename;
        myPics.append(img);
      });
    };
    myFolders.append(fi);
  });
}
